# Η τιμή των χιλιομέτρων ανα λίτρο(ΧΑΛ) υπολογίζεται ως εξής:
# ΧΑΛ = (Διανυθέντα χιλιόμετρα)/(Λίτρα βενζίνης που καταναλώθηκαν)
xiliometra = float(input('Δώσε τα Διανυθέντα χιλιόμετρα: '))
litra = float(input('Δώσε τα Λίτρα βενζίνης που καταναλώθηκαν: '))
XAL = float(xiliometra / litra)
print('Η τιμή των ΧΑΛ είναι: ', format(XAL, '.2f'), sep='')
