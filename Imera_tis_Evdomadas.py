arithmos = int(input('Δώσε έναν αριθμό από το 1 έως το 7: '))
if arithmos == 1:
    print('Δευτέρα')
elif arithmos == 2:
    print('Τρίτη')
elif arithmos == 3:
    print('Τετάρτη')
elif arithmos == 4:
    print('Πέμπτη')
elif arithmos == 5:
    print('Παρασκευή')
elif arithmos == 6:
    print('Σάββατο')
elif arithmos == 7:
    print('Κυριακή')
else:
    print('Έχετε δώσει αριθμό εκτός του διαστήματος 1 έως 7. '
          'Ξανά προσπαθήστε.')
