# Απόσταση= Ταχύτητα * χρόνος
# Το αυτοκίνητο ταξιδεύει με 100 χιλιόμετρα την ώρα
ap1 = float(100 * 5)
ap2 = float(100 * 8)
ap3 = float(100 * 12)
print('Η απόσταση που θα διανύσει το αυτοκίνητο σε 5 ώρες είναι: ',
      ap1, 'km/h', sep='')
print('Η απόσταση που θα διανύσει το αυτοκίνητο σε 8 ώρες είναι: ',
      ap2, 'km/h', sep='')
print('Η απόσταση που θα διανύσει το αυτοκίνητο σε 12 ώρες είναι: ',
      ap3, 'km/h', sep='')
